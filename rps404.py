class Player:
    def __init__(self):
        self.score = 0
        self.own_previous_move = choice (ACTIONS)
        self.other_previous_move = choice (ACTIONS)

    def move(self):
        return "rock"

    def learn(self, my_move, their_move):
        self.own_previous_move = my_move
        self.other_previous_move = their_move

class NPC(Player):
    def move(self):
        return choice(ACTIONS)

class ReflectPlayer(Player):
    def move(self):
        return self.other_previous_move

class CyclePlayer(Player):
    def move(self):
        index = ACTIONS.index(self.own_previous_move)
        if index == 2:
            return ACTIONS[0]
        else:
            return ACTIONS[index + 1]

